    local width=$(tput cols 2>/dev/null || echo 80)

    if [ "$width" -ge 90 ]; then
        enhanced_concept2  # Full bordered version for large screens
    elif [ "$width" -ge 70 ]; then
        enhanced_concept1  # Clean version for medium screens
